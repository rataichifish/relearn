# 将华氏温度转换成摄氏温度
print("练习2：将华氏温度转换成摄氏温度")
F = int(input("请输入华氏摄氏度："))     # 华氏温度
C = int((F-32)*5/9)                      # 摄氏温度
print("转换后的摄氏度为：",C)
